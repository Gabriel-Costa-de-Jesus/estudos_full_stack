num_fatorial = int(input("Digite um número Fatorial: "))
multi = 1
while num_fatorial > 0:
    print(multi, "*", num_fatorial)
    multi *= num_fatorial
    num_fatorial -= 1

print(f"O número Fatorial é {multi}")
