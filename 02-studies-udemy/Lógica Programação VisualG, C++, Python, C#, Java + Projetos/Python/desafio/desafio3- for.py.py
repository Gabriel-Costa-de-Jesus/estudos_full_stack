'''
Exercício: Praticando a Estrutura de Controle For em Python



Neste exercício, você vai criar um programa que imprime os números de 1 a 10 utilizando o loop for.



Os passos a seguir o ajudarão a construir a sua solução:



Primeiramente, utilize a função range para gerar uma sequência de números de 1 a 10. Lembre-se, em Python, a função range gera números até, mas não incluindo, o final do intervalo. Portanto, para obter números de 1 a 10, você precisará usar range(1, 11).

Em seguida, utilize o loop for para iterar sobre essa sequência de números.

Dentro do loop for, utilize a função print para imprimir o número atual.



Boa sorte!
'''
for i in range(1, 10+1, 1):
    print(i)